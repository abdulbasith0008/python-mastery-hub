'''3. Operators Usage
Problem:
Write a Python program to take two numbers as input from the user and perform arithmetic operations (+, -, *, /, %, **).'''

user1=int(input("Enter Number one:"))
user2=int(input("Enter Number two:"))
print("Sum",user1+user2)
print("Difference",user1-user2)
print("Multiply",user1*user2)
print("Division",user1/user2)
print("Remainder",user1%user2)
print("Exponential",user1**user2)